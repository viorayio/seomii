<p><b>Dit Privatliv:</b><br/>
Her er, hvordan vi behandler dine personlige data, når du benytter vores hjemmeside.
<br/><br/>
Vi benytter cookies, som er små passive filer, der indhenter information. Cookies kan ikke sprede virus eller indeholde information som direkte kan identificere dig, men de kan give dig en mere personificeret web brugeroplevelse. Du kan tillade nødvendige cookies eller alle cookies. Du kan læse mere om vores Cookie Politik.
<br/><br/>
<b>Nødvendige cookies:</b>
<br/>
Cookies herunder er nødvendige for at sitet fungerer. Hjemmesiden kan ikke fungere korrekt uden dem og du kan derfor ikke fravælge dem i vores system. De husker ofte dine handlinger på sitet, om du er logget ind og om du har udfyldt formularer.
<br/>
<br/>
<b>Præferencecookies:</b>
<br/>
Præferencecookies gør, at vi kan huske de valg, du har foretaget dig på sitet såsom valg af sprog, og det browserlayout du foretrækker på websiden såsom antal og størrelsen af viste billeder.
<br/><br/>
<b>Statistik cookies:</b>
<br/>
Disse cookies hjælper os med at redegøre for din interaktion med websitet. Enhver information indhentes anonymt. Formålet er at forstå, hvordan du interagerer for at forbedre websitets funktioner.
</p>

